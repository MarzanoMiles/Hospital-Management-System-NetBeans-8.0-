    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword());

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter username and password", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        DataStore dataStore = DataStore.getInstance();
        List<User> users = dataStore.getUsers();
        
        User loggedInUser = null;
        for (User user : users) {
            if (user.getUsername().equals(username) && user.getPassword().equals(password)) {
                loggedInUser = user;
                break;
            }
        }

        if (loggedInUser == null) {
            JOptionPane.showMessageDialog(this, "Invalid username or password", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Route to appropriate dashboard based on role
        String role = loggedInUser.getRole();
        
        if (role.equals("ADMIN")) {
            new AdminDashboard(username).setVisible(true);
        } else if (role.equals("CO_ADMIN")) {
            new CoAdminDashboard(username).setVisible(true);
        } else if (role.equals("DOCTOR")) {
            new DoctorDashboard(username).setVisible(true);
        } else if (role.equals("NURSE")) {
            new NurseDashboard(username).setVisible(true);
        } else if (role.equals("CASHIER")) {
            new CashierDashboard(username).setVisible(true);
        }
        
        this.dispose();
    }//GEN-LAST:event_btnLoginActionPerformed

