    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword());

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter username and password", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        DataStore dataStore = DataStore.getInstance();
        Signup signup = dataStore.patientLogin(username, password);

        if (signup == null) {
            JOptionPane.showMessageDialog(this, "Invalid credentials or account not approved", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (signup.getPatientId() == null) {
            JOptionPane.showMessageDialog(this, "Your account is pending approval", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        new PatientDashboard(signup).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnLoginActionPerformed

